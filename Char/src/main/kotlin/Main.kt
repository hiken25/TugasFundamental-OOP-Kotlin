package org.example

//TIP To <b>Run</b> code, press <shortcut actionId="Run"/> or
// click the <icon src="AllIcons.Actions.Execute"/> icon in the gutter.
fun main() {
    var nilai = 'A'

    println("Sebelum kenaikan: Nilai $nilai")
    nilai++
    println("Setelah kenaikan pertama: Nilai $nilai")
    nilai++
    println("Setelah kenaikan kedua: Nilai $nilai")
    nilai++
    println("Setelah kenaikan ketiga: Nilai $nilai")

    println("")

    println("Sebelum penurunan: Nilai $nilai")
    nilai--
    println("Setelah penurunan pertama: Nilai $nilai")
    nilai--
    println("Setelah penurunan kedua: Nilai $nilai")
    nilai--
    println("Setelah penurunan ketiga: Nilai $nilai")
}